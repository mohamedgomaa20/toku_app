class User {
  final String image;
  final String developerName;
  final String joTitle;
  final String bio;
  final String email;
  final String phone;
  final String github;
  final String linkedin;
  final String website;

  const User({
    required this.image,
    required this.developerName,
    required this.joTitle,
    required this.bio,
    required this.email,
    required this.phone,
    required this.github,
    required this.linkedin,
    required this.website,
  });
}
